
public class Human {
	String name;
	public Human(String name) {
	  this.name=name;
	}
	public void drinkWater() {
		System.out.println( " " +name + " drinking water...");
	}
	
	public void eat() {
		System.out.println( " " +name + " eating...");
	}
	
	public void speak() {
		System.out.println( " " +name + " speak...");
	}
	
	public void sit() {
		System.out.println( " " +name + " sitting...");
	}
	
}
